#atribuindo valores para variáveis
a = 2
b = 3

#operação de adição
resultado = a + b
print(resultado);

#operação de subtração
resultado = a - b
print(resultado);

#operação de multiplicação
resultado = a * b
print(resultado);

#operação de divisão
resultado = a / b
print(resultado);

#operação de resto da divisão
resultado = a % b
print(resultado);

#operação de exponenciação
resultado = a ** b
print(resultado);

#operação de dividão de números inteiros
resultado = a // b
print(resultado);



